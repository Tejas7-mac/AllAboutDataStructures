package LinkedList;

/**
 * This is the Node class , basically used to create a node to store data and point it to next node i.e (storing the reference of the next nod in the present Node).
 */

public class Node {
    /**
     * we can create our own data type like string, float etc. based on the requirement.
     * here i am using int data type.
     */
    int data;
    /**
     * The node here is the reference for the next node.
     */
    Node next;
}
